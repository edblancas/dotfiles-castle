set('v', '<space>x', ':lua<CR>', { desc = 'Lua: source this selection' })
set("n", "<M-j>", "<cmd>cnext<CR>", { desc = 'Next quick list entry' })
set("n", "<M-k>", "<cmd>cprev<CR>", { desc = 'Previous quick list entry' })
set("n", "-", "<cmd>Oil<CR>", { desc = 'Oil' })
